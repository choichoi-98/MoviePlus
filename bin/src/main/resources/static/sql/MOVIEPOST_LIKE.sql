CREATE TABLE MOVIEPOST_LIKE (
 	MOVIEPOST_LIKE_NUM NUMBER PRIMARY KEY,
 	MEMBER_ID VARCHAR2(100),
 	MOVIEPOST_NUM NUMBER,	
 	FOREIGN KEY (MEMBER_ID) REFERENCES MEMBER(MEMBER_ID) ON DELETE CASCADE,
 	FOREIGN KEY (MOVIEPOST_NUM) REFERENCES MOVIEPOST(MOVIEPOST_NUM) ON DELETE CASCADE
);

CREATE SEQUENCE MOVIEPOST_LIKE_SEQ;